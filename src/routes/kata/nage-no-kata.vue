      <h2>Nage no Kata</h2>
      <p>
        Nage no Kata, also called Randori no Kata, was developed in 1884 and
        1885 at the Kodokan. It was created by Jigaro Kano to help judo students
        understand the principles and master the basic techniques of nage waza
        used in randori. It is made up of three representative techniques
        selected from te-waza, koshi-waza, ashi-waza, ma-sutemi-waza, and
        yoko-sutemi-waza to practice both left and right sides in each
        technique. Basis of throwing techniques is acquired by practice of
        kuzushi (breaking of balance), tsukuri (set up to execute the
        technique), kake (execution of the technique), and how to receive the
        technique.
      </p>
