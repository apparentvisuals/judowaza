      <h2>Key Points</h2>
      <table class="key-points border border-collapse table-auto w-full">
        <tbody>
          <tr>
            <th>General Principles</th>
            <td>
              <ul>
                <li>Demonstrate confidence (spirit) and control.</li>
                <li>
                  Memorize the order of kata so that attention can be focused on
                  the details.
                </li>
                <li>
                  Kata should be performed smoothly without unnecessary
                  movements.
                </li>
                <li>
                  Unless uke is striking, tsugi ashi is the form of walking
                  (except tomoe nage).
                </li>
                <li>
                  Uke initiates grip or attack, but tori should always lead the
                  movement.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Uke</th>
            <td>
              <ul>
                <li>
                  Uke must really aim to strike the tento (forehead), with
                  conviction.
                </li>
                <li>
                  Uke must learn how to breakfall properly, both laying down and
                  rolling up.
                </li>
                <li>
                  Uke maintains a straight, strong body during the throw and in
                  breakfall.
                </li>
                <li>
                  All throws are throws, there is no need for uke to ever jump.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Tori</th>
            <td>
              <ul>
                <li>
                  Demonstrate kuzushi, tsukuri and kake in all throws, key
                  transferable skills.
                </li>
                <li>
                  Kuzushi: Demonstrate the off balancing by showing uke at an
                  angle.
                </li>
                <li>
                  Tsukuri: Get under uke’s centre of gravity. Not collision
                  based judo, don’t hit
                </li>
                <li>
                  Kake: Apply the proper technique. Power is not strength alone.
                </li>
                <li>
                  In practice, you should be able to lift uke without throwing.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Bow</th>
            <td>
              <ul>
                <li>Straight back.</li>
                <li>Keep your hips low for zarei.</li>
                <li>
                  Keep the neck, head, and eyes at the same level throughout the
                  bow.
                </li>
              </ul>
            </td>
          </tr>
        </tbody>
        <tr>
          <th colspan="2">Te Waza</th>
        </tr>
        <tbody>
          <tr>
            <th>Uki Otoshi</th>
            <td>
              <ul>
                <li class="beg">
                  Change of direction from pulling back to down.
                </li>
                <li class="int">
                  Progressive unbalancing of uke, demonstrating kuzushi with a
                  small lean.
                </li>
                <li class="int">
                  Tori throws with both hands to the side while maintaining
                  balance.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Seoi Nage</th>
            <td>
              <ul>
                <li class="beg">
                  Tori blocks attack and steps in with the opposite foot. Use
                  that foot to pivot in for your attack.
                </li>
                <li class="int">
                  Uke attacks tori’s tento with an overhead strike while
                  maintaining a straight strong body until ukemi.
                </li>
                <li class="int">
                  Tori blocks attack but does not stop the momentum and uses it
                  to throw.
                </li>
                <li class="int">
                  Non-blocking hand reaches up as high as possible to grab uke’s
                  shoulder.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Kata Guruma</th>
            <td>
              <ul>
                <li class="beg">
                  Tori should pull uke onto their shoulder and not try to bend
                  under or towards uke but squat with the legs.
                </li>
                <li class="int">
                  Leg hand should not grab but should be able to stay loose as a
                  guide only.
                </li>
                <li class="int">
                  The throw is a drop in front of tori at a 45 angle.
                </li>
              </ul>
            </td>
          </tr>
        </tbody>
        <tr>
          <th colspan="2">Koshi Waza</th>
        </tr>
        <tbody>
          <tr>
            <th>Uki Goshi</th>
            <td>
              <ul>
                <li class="beg">
                  Lower your shoulder to avoid strike and pull uke’s hips in,
                  hand on the belt.
                </li>
                <li class="int">
                  Right side should be done walking towards each other as uke
                  strikes.
                </li>
                <li class="int">
                  Feet at 45 and do not move, small circle whip of upper body.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Harai Goshi</th>
            <td>
              <ul>
                <li class="beg">
                  On the 3rd step, tori pulls uke in, not towards uke, body
                  turns in sync.
                </li>
                <li class="int">
                  Both hands should be used to pull uke into an off-balanced
                  position.
                </li>
                <li class="int">
                  Attacking leg should maintain contact with the ground and
                  strike back quickly in order to regain balance after the
                  throw.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Tsurikomi Goshi</th>
            <td>
              <ul>
                <li class="beg">
                  1st step of 3rd move needs to be confident, pull up and step
                  across inside uke’s feet and in front of uke’s toes.
                </li>
                <li class="int">
                  Maintain space between uke and tori until the very end.
                </li>
                <li class="int">
                  The high grip hand is for guiding, the throw happens by
                  raising tori’s hips.
                </li>
              </ul>
            </td>
          </tr>
        </tbody>
        <tr>
          <th colspan="2">Ashi Waza</th>
        </tr>
        <tbody>
          <tr>
            <th>Okuri Ashi Harai</th>
            <td>
              <ul>
                <li class="beg">
                  Legs should be slightly bent with little to no up-down motion
                  through the side-step movement. Sweeping foot should be close
                  to ground.
                </li>
                <li class="int">
                  Starting position closer than other techniques; arms should be
                  slightly bent.
                </li>
                <li class="int">
                  1st side step is small, then slight pause, 2nd-3rd in
                  progressively larger steps.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Sasai Tsurikomi Ashi</th>
            <td>
              <ul>
                <li class="beg">
                  Sleeve arm should pull back and stay at eye level.
                </li>
                <li class="int">
                  On completing the motion of 2nd step, tori’s back foot should
                  continue at a 45 angle behind and to the outside (no clear
                  3rd step).
                </li>
                <li class="int">
                  Center of rotation should on back foot and pivot as uke is
                  pulled over the out stretched attacking leg.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Uchi Mata</th>
            <td>
              <ul>
                <li class="beg">
                  Grip and arm movement is circular and horizontal.
                </li>
                <li class="int">
                  3rd move for tori is a small step with feet but regular upper
                  body.
                </li>
                <li class="int">
                  Uke and tori should be close on 3rd movement before lifting
                  leg, tori should feel some of uke’s weight resting on upper
                  body area.
                </li>
              </ul>
            </td>
          </tr>
        </tbody>
        <tr>
          <th colspan="2">Ma Sutemi Waza</th>
        </tr>
        <tbody>
          <tr>
            <th>Tomoe Nage</th>
            <td>
              <ul>
                <li class="int">
                  Meet 1m from centre towards uke, tori pushes uke back for
                  three steps (ayumi ashi).
                </li>
                <li class="int">
                  Tori squats under uke’s centre of gravity while switching to a
                  double lapel grip and progressively unbalances uke,
                  demonstrating kuzushi with a small lean back.
                </li>
                <li class="int">
                  Tori’s foot comes up to uke’s lower hip and throws with both
                  hands while rolling back. Then tori maintains a bridge
                  position.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Ura Nage</th>
            <td>
              <ul>
                <li class="int">
                  Uke attacks tori’s tento with an overhead strike while
                  maintaining a straight strong body until ukemi.
                </li>
                <li class="int">
                  Tori ducks under the attack, leading with the foot closest to
                  joseki; one hand behind uke on the belt and one hand in front
                  of uke on the belt.
                </li>
                <li class="int">
                  Tori does not stop the momentum and instead uses it to lift
                  and throw backwards while arching back. Then tori maintains a
                  bridge position.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Sumi Gaeshi</th>
            <td>
              <ul>
                <li class="int">
                  Tori and uke meet 1m from centre towards uke and then enter
                  jigotai.
                </li>
                <li class="int">
                  Tori squats under uke’s centre of gravity and progressively
                  unbalances uke, demonstrating kuzushi with a small lean back.
                </li>
                <li class="int">
                  Tori uses the inside of their ankle to hook the lower thigh of
                  uke and throws with both hands while rolling back. Then tori
                  maintains a bridge position.
                </li>
              </ul>
            </td>
          </tr>
        </tbody>
        <tr>
          <th colspan="2">Yoko Sutemi Waza</th>
        </tr>
        <tbody>
          <tr>
            <th>Yoko Gake</th>
            <td>
              <ul>
                <li class="int">
                  With each step back, tori should progressively pull uke off
                  balance backwards and to the side.
                </li>
                <li class="int">
                  Third step slightly slows then lean before tori does a quick
                  switchfoot and sweeps through both ankles.
                </li>
                <li class="int">
                  Tori lands on their side while uke lands flat on their back
                  with feet slightly elevated.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Yoko Guruma</th>
            <td>
              <ul>
                <li class="int">
                  Uke attacks tori’s tento with an overhead strike while tori
                  ducks under attempting another ura nage.
                </li>
                <li class="int">
                  Uke counters ura nage by dropping their upper body weight, and
                  moving their striking arm forward and to the side.
                </li>
                <li class="int">
                  Tori then counters uke’s counter by sliding their supporting
                  leg in-between uke’s legs, throwing with a slide to the side
                  and both hands used for throwing.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Uki Waza</th>
            <td>
              <ul>
                <li class="int">
                  Tori and uke meet 1m from centre towards uke and then enter
                  jigotai.
                </li>
                <li class="int">
                  Uke resists what looks like another sumi gaeshi by trying to
                  regain balance.
                </li>
                <li class="int">
                  Tori responds by breaking uke’s balance diagonally by sliding
                  out their leg and lying down on their side in order to throw
                  uke diagonally back.
                </li>
              </ul>
            </td>
          </tr>
        </tbody>
