            <th>Uke</th>
            <td>
              <ul>
                <li>
                  Uke must really aim to strike the tento (forehead), with
                  conviction.
                </li>
                <li>
                  Uke must learn how to breakfall properly, both laying down and
                  rolling up.
                </li>
                <li>
                  Uke maintains a straight, strong body during the throw and in
                  breakfall.
                </li>
                <li>
                  All throws are throws, there is no need for uke to ever jump.
                </li>
              </ul>
            </td>
          </tr>
          <tr>
            <th>Tori</th>
            <td>
              <ul>
                <li>
                  Demonstrate kuzushi, tsukuri and kake in all throws, key
                  transferable skills.
                </li>
                <li>
                  Kuzushi: Demonstrate the off balancing by showing uke at an
                  angle.
                </li>
                <li>
                  Tsukuri: Get under uke’s centre of gravity. Not collision
                  based judo, don’t hit
                </li>
                <li>
                  Kake: Apply the proper technique. Power is not strength alone.
                </li>
                <li>
                  In practice, you should be able to lift uke without throwing.
                </li>
              </ul>
            </td>
          </tr>
