      <h2>Tips to Train and Study Kata</h2>
      <ol>
        <li>
          Memorize the order of throws for each set of Nage no kata. If you
          truly know the order, then you can be confident and focus on the
          details to improve each movement.
        </li>
        <li>
          Focus on improving your kuzushi, tsukuri, and kake. This will greatly
          improve your kata and will improve your judo for randori and shiai as
          well.
        </li>
        <li>
          Perform each movement as you would uchi komi – repeat, repeat, repeat!
          You should not be doing a complete run of the kata over and over.
          Break up each movement, and set, to perform it as uchi komi in order
          to practice, build up muscle memory, and improve the details.
        </li>
        <li>
          Nage no kata can be practiced without a partner. Walk through the kata
          for practice or warm-up to help train your body and mind.
        </li>
        <li>
